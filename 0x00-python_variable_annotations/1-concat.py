def concat(str1: str, str2: str) -> str:
    """Type-annotated function that takes a string str1 and a string
    str2 as arguments and returns a concatenated string.

    Args:
        str1 (str): first string to concatenate.
        str2 (str): second string to concatenate.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
